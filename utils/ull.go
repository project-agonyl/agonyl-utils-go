// DecodeULL decodes the first size bytes of buffer in-place using the ULL algorithm.
// buffer is modified; size must be in range [0, len(buffer)].
func DecodeULL(buffer []byte, size int) {
	for i, j := 1, size-1; i < size; i, j = i+1, j-1 {
		buffer[j] = byte(byte(i) ^ (byte(size) ^ (cryptTable[buffer[j-1]] ^ buffer[j])))
	}
}

// EncodeULL encodes the first size bytes of buffer in-place using the ULL algorithm.
// It is the inverse of DecodeULL: Encode(Decode(buf)) and Decode(Encode(buf)) restore the buffer.
// buffer is modified; size must be in range [0, len(buffer)].
// Process order is low-to-high (1..size-1) so each step uses the already-encoded value at i-1.
func EncodeULL(buffer []byte, size int) {
	for i := 1; i < size; i++ {
		buffer[i] = byte(byte(size-i) ^ (byte(size) ^ (cryptTable[buffer[i-1]] ^ buffer[i])))
	}
}
